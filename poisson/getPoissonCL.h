#include "TMath.h"
#include <iostream>
#include <cmath>

//################################################################################################################################
//################################################################################################################################
double getOneSidedUpperLimit(double mu, double cl){

  double muUp = 1./2.*TMath::ChisquareQuantile(cl,2.*(mu+1));
  //cout<<"Upper limit ("<<100*(cl)<<"%) = "<<muUp<<endl;


  return muUp;

}    
//################################################################################################################################
//################################################################################################################################
double getOneSidedLowerLimit(double mu, double cl){

  double muLow = 1./2.*TMath::ChisquareQuantile(1-cl,2.*mu);
  //cout<<"Lower limit ("<<100*(cl)<<"%) = "<<muUp<<endl;

  return muLow;

}    
